uint64
sys_getpids(void) {
    uint64 pids_addr;
    uint64 names_addr;
    int max;

    argint(2, &max);
    argaddr(1, &names_addr);
    argaddr(0, &pids_addr);

    if ((max < 0) || (pids_addr < 0) || (names_addr < 0))
      return -1;

    int pids[max];
    char names[max * 16];
    int count = getpids(pids, names, max);

    if(copyout(myproc()->pagetable, pids_addr, (char *)pids, sizeof(int) * count) < 0 ||
      copyout(myproc()->pagetable, names_addr, names, count * 16) < 0)
      return -1;

    return count;
}

uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  if(n < 0)
    n = 0;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}
